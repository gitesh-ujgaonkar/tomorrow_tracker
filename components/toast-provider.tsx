'use client';

import { useToast as useToastPrimitive } from "@/hooks/use-toast";

export function useToast() {
  const { toast } = useToastPrimitive();
  
  return {
    toast: (options: { 
      title: string; 
      description?: string; 
      variant?: 'default' | 'destructive';
    }) => {
      toast({
        title: options.title,
        description: options.description,
        variant: options.variant || 'default',
      });
    },
  };
}

